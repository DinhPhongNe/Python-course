a = float(input("Nhập giá trị của a: "))
b = float(input("Nhập giá trị của b: "))

tong = a + b #cộng
hieu = a - b #trừ
tich = a * b #nhân
thuong = a / b # chia

print("Tổng của a và b là:", tong)
print("Hiệu của a và b là:", hieu)
print("Tích của a và b là:", tich)
print("Thương của a và b là:", thuong)