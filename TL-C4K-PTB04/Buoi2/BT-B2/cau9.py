d = float(input("Nhập độ dài: "))
r = float(input("Nhập chiều rộng: "))

chu_vi = 2 * (d + r) # công thức tính chu vi
dien_tich = d * r # công thức tính diện tích

print(f"Chu vi của hình chữ nhật là: {chu_vi}") #in ra chu vi
print(f"Diện tích của hình chữ nhật là: {dien_tich}") #in ra diện tích